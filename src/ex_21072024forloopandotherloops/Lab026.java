package ex_21072024forloopandotherloops;

public class Lab026 {
    public static void main(String[] args) {
         for (int i = 0; i<5 ;i++){
             System.out.println(i);
         }

         //loops function

         for(float f = 0;f<10.20; f++ ) {
             System.out.println("Hi Shan ->"+f);
         }

         for(long c = 0;c<20; c++) {
             System.out.println("Hi Gopal ->"+c);
         }

         for(char c = 'a'; c<='z'; c++) {
             System.out.println(c);
        }

    }
}
