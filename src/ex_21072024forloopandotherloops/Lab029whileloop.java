package ex_21072024forloopandotherloops;

public class Lab029whileloop {
    public static void main(String[] args) {
        int i = 0;
        while (i <=10){
            System.out.println(i);
            i++;
        }
    }
}
// While Loop - A,B,C
// where A - Initiator, B - Condition, C- Increment/Decrement
